/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    private long[] postOrder(TreeNode root) {
        if (root == null) {
            return new long[]{0, 0};
        }

        long[] l = postOrder(root.left);
        long[] r = postOrder(root.right);

        long rob = root.val + l[0] + r[0];

        long notRob = Math.max(l[0], l[1]) + Math.max(r[0], r[1]);
        return new long[]{notRob, rob};
    }

    public int rob(TreeNode root) {
        long[] ans = postOrder(root);
        return (int) Math.max(ans[0], ans[1]);
    }
}
