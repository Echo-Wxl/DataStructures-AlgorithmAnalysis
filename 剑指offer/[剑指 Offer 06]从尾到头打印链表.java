/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public int[] reversePrint(ListNode head) {
        Stack<ListNode> stack = new Stack<>();
        while (head != null) {
            ListNode next = head.next;
            stack.push(head);
            head = next;
        }

        int size = stack.size();
        int[] ans = new int[size];
        int i = 0;
        while (!stack.isEmpty()) {
            ans[i++] = stack.pop().val;
        }
        return ans;
    }
}
