class Solution {
    public int majorityElement(int[] nums) {
        final int N = nums.length;
        int mid = N/2;

        Map<Integer, Integer> map = new HashMap<>();
        for (int i = 0; i < N; i++) {
            int count = map.getOrDefault(nums[i], 0);
            map.put(nums[i], count+1);
            if (count+1 > mid) {
                return nums[i];
            }
        }
        return -1;
    }
}
