class Solution {
    private void swap(int[] arr, int i, int j) {
        int temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
    }
    public void moveZeroes(int[] nums) {
        int l = 0, i = 0;
        while (i < nums.length) {
            if (arr[i] != 0) {
                swap(arr, i++, l++);
            } else {
                i++;
            }
        }
    }
}
