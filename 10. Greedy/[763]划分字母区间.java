class Solution {
    public List<Integer> partitionLabels(String S) {
        final int N = S==null ? 0:S.length();
        int[] pos = new int[26];
        for (int i = 0; i < N; i++) {
            pos[S.charAt(i) - 'a'] = i;
        }
        int end = 0;
        int start = 0;
        List<Integer> ans = new ArrayList<>();
        for (int i = 0; i < N; i++) {
            end = Math.max(end, pos[S.charAt(i) - 'a']);
            if (i == end) {
                ans.add(end-start+1);
                start = end+1;
            }
        }
        return ans;
    }
}
