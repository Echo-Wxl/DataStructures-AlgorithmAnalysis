class Solution {
    public int minDistance(String word1, String word2) {
        final int N = word1.length();
        final int M = word2.length();

        if (N == 0 || M == 0) {
            return 0;
        }
        int[][] dp = new int[N + 1][M + 1];
        for (int i = 1; i <= N; i++) {
            for (int j = 1; j <= M; j++) {
                if (word1.charAt(i - 1) == word2.charAt(j - 1)) {
                    dp[i][j] = dp[i - 1][j - 1] + 1;
                } else {
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
                }
            }
        }
        return N + M - 2 * dp[N][M];
    }
}
