class Solution {
    public String longestPalindrome(String s) {
        String orign = s;
        String reverse = new StringBuffer(orign).reverse().toString();

        int N = s.length();
        int[][] dp = new int[N][N];
        int maxLen = 0;
        int maxEnd = 0;
        for (int i = 0; i < N; i++) {
            for (int j = 0; j < N; j++) {
                if (orign.charAt(i) == reverse.charAt(j)) {
                    if (i == 0 || j == 0) {
                        dp[i][j] = 1;
                    } else {
                        dp[i][j] = dp[i - 1][j - 1] + 1;
                    }
                }

                if (dp[i][j] > maxLen) {
                    int beforePos = N-1-j;
                    if (beforePos + dp[i][j] - 1 == i) {
                        maxLen = dp[i][j];
                        maxEnd = i;
                    }
                }
            }
        }
        return s.substring(maxEnd-maxLen+1, maxEnd+1);
    }
}
